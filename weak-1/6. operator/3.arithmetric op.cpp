#include <iostream>
using namespace std;
int main()
{
    // arithmetic opertors.
    int a = 10;
    int b = 5;
    cout << (a + b) << endl;
    cout << (a - b) << endl;
    cout << (a * b) << endl;
    cout << (a / b) << endl;
    cout << (a % b) << endl;       // gives us remainder ..
    
    cout << (5.0 / 2) << endl;     //-2.5
    cout << (5 / 2) << endl;       //-2
    cout << (5 * 1.0 / 2) << endl; //-2.5
    return 0;
}